// [raise] or [raise, call] — fold は自動計算
type Spec = number | [number] | [number, number]

function build(spec: Record<string, Spec>): Record<string, RangeCell> {
  return Object.fromEntries(
    Object.entries(spec).map(([hand, v]) => {
      const raise = typeof v === 'number' ? v : v[0]
      const call = Array.isArray(v) && v.length > 1 ? (v as [number, number])[1] : 0
      return [hand, { hand, raise, call, fold: +(1 - raise - call).toFixed(2) }]
    }),
  )
}

